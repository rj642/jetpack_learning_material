object XMLEquivalents {

    /**
     * Row - Row is equivalent to LinearLayout with orientation set
     * to horizontal
     *
     * Column - Column is equivalent to LinearLayout with orientation set
     * to Vertical
     *
     * LazyColumn - LazyColum is equivalent to RecyclerView with layoutManager
     * set to LinearLayoutManager and orientation set to vertical
     *
     * LazyRow - LazyColum is equivalent to RecyclerView with layoutManager
     * set to LinearLayoutManager and orientation set to horizontal
     *
     * Modifier - Modifiers are basically essential property of a layout such as layout_width and layout_height
     * along with padding
     *
     * Margin - There is no margin available and hence padding works as a margin and padding both
     */


}